int n, m;
int a[N];
int blk[N];
const int sz=707;
struct Query{
	int l, r, id;
	Query(){}
	bool operator < (const Query& rhs){
		return blk[l]==blk[rhs.l] ? (blk[l]&1 ? r>rhs.r: r<rhs.r) : l<rhs.l;
	}
}Q[N];

stack<int> s;
int ins[N], cnt[N], ans[N];
void update(int i, int op){
	cnt[a[i]]+=op;
	if(cnt[a[i]]==1 && !ins[a[i]]){
		ins[a[i]]=1;
		s.push(a[i]);
	}
}

int main(){
	ios::sync_with_stdio(0),cin.tie(0),cout.tie(0);
	cin>>n;
	for(int i=1;i<=n;i++){
		cin>>a[i];
		blk[i]=i/sz;
	}
	cin>>m;
	for(int i=0;i<m;i++){
		cin>>Q[i].l>>Q[i].r;
		Q[i].id=i;
	}
	sort(Q, Q+m);
	int l=1, r=0;
	for(int i=0;i<m;i++){
		while(l < Q[i].l){
			update(l++, -1);
		}
		while(l > Q[i].l){
			update(--l, 1);
		}
		while(r < Q[i].r){
			update(++r, +1);
		}
		while(r > Q[i].r){
			update(r--, -1);
		}
		while(SZ(s)&&cnt[s.top()] != 1){
			ins[s.top()]=0;
			s.pop();
		}
		if(SZ(s)){
			ans[Q[i].id]=s.top();
		}
		else{
			ans[Q[i].id]=0;
		}
	}
	for(int i=0;i<m;i++)
		printf("%d\n", ans[i]);
}

