void modify(int p, int x){
	p+=h;
	tr[p]+=x;
	mx[p]=tr[p];
	for(p>>=1;p;p>>=1){
		tr[p]=tr[p<<1]+tr[p<<1|1];
		mx[p]=min(mx[p<<1|1], mx[p<<1] + tr[p<<1|1]);
	}
}

int query(){
	int u=1, w=0;
	while(u<h){
		if(mx[u<<1|1]<w){
			u=u<<1|1;
		}
		else{
			w-=tr[u<<1|1];
			u=u<<1;
		}
	}
	return u==h?-1:u-h;
}

