int n, m, d;
int p[N];
int c[N];
int b[N];
bool stay[N];
vector<int> g[N];
int match[N], ans[N];
int vis[N];
int df;

bool dfs(int u){
	if(vis[u]==df)return 0;
	vis[u]=df;
	for(const int& v:g[u]){
		if(match[v]==-1 || dfs(match[v])){
			match[v]=u;
			return 1;
		}
	}
	return 0;
}

